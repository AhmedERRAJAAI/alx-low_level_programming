/**
 * op_add - add a and b
 *
 * @a: int
 * @b: int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * oper_sub - substraction
 *
 * @a: int
 * @b: int
 * Return: int
 */
int oper_sub(int a, int b)
{
	return (a - b);
}

/**
 * oper_mul - multiplication
 *
 * @a: int
 * @b: int
 * Return: int
 */
int oper_mul(int a, int b)
{
	return (a * b);
}

/**
 * oper_div - division
 *
 * @a: int
 * @b: int
 * Return: int
 */
int oper_div(int a, int b)
{
	return (a / b);
}

